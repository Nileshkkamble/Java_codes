class Switch_case{
  public static void main(String args[]){
    int a = 2;
    switch(a){
      case(1):
        System.out.println("this is first case");
        break;
      case(2):
        System.out.println("this is second case");
        break;
      default:
        System.out.println("this is default case");
    }
  }
}
    
